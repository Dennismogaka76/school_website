<p>Former East Africa champions St Anthiny launched their quest for this year’s Brookside East Africa Secondary Schools boys’ basketball title with victory as the games began at Kaunda Grounds in Gulu, Uganda.

The 2013 regional champions beat Rwanda’s Eteni 59-49 in their opening Group B match to increase their chances of qualifying for the knockout stage.

Both teams had a slow start and took longer to settle. But it was the Kenyans, who had the upper hand at the end of the first quarter, which they won 9-6. With just one and a half baskets separating the two sides in the second quarter,St Anthony maintained their cool to win the second quarter 16-7 for a 25-13 lead at half time.

Perhaps comfortable with a six-basket lead, the Kenyans relaxed in the third quarter, but still won 13-9 as the Rwandans, who kept improving with every quarter, fought harder.

Eteni came back stronger in the fourth quarter and gave Upper Hill a tough time as they won 27-20, but it was a little too late for the Rwandans.
